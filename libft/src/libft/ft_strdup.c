#include "../../include/libft.h"

char	*ft_strdup(const char *str)
{
	char	*dest;
	int		i;
	int		size;

	i = 0;
	size = ft_strlen(str);
	dest = (char *)malloc(sizeof(char) * (size + 1));
	if (!dest)
		return (NULL);
	while (str[i])
	{
		dest[i] = str[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
